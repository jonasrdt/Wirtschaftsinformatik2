
def calculatesum(zahl1, zahl2):
    """
    The function "calculatesum" takes two numbers as input, adds them together, and returns the result.
    
    :param zahl1: The first number to be added in the calculation
    :param zahl2: The parameter "zahl2" is a variable that represents the second number that will be
    added to the first number (represented by the parameter "zahl1") in the function "calculatesum"
    :return: the sum of the two input numbers (zahl1 and zahl2).
    """
    return zahl1 + zahl2

print(calculatesum(5,10))

