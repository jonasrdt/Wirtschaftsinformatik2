
# Definition von Variablen des Datentyps String (str)
# Es ist egal, welche Art von Anführungszeichen genutzt werden
vorname = input("Bitte geben Sie Ihren Vornamen ein: ")
nachname = input("Bitte geben Sie Ihren Nachnamen ein: ")

# Definition einer Variable des Datentyps Integer (int)
alter = int(input("Bitte geben Sie Ihr Alter ein: "))

# Mehrfachausgabe von zwei Strings (str) und einem Integer (int).
print(vorname, nachname , "ist", alter, "Jahre alt.")
print("In fünf Jahren ist", vorname, alter + 5, "Jahre alt.")