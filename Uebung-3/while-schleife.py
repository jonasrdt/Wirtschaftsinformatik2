

# while Schleifen - läuft solange, wie die mit ihr verbundene Bedingung wahr ist
# und die Abbruchbedingung noch nicht ausgelöst wurde.

arbeitsstunden = 1

while arbeitsstunden <= 5:
    print("Hallo, ich bin eine while-Schleife. Ich zähle die verstrichenen Arbeitsstunden.")
    print("Du arbeitest seit", arbeitsstunden, "Stunden.")
    print("¯\_(ツ)_/¯")
    arbeitsstunden = arbeitsstunden + 1
