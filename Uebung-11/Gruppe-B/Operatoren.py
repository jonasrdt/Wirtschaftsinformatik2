zahl_1 = 5
zahl_2 = 10
# Klassische Vergleichsoperatoren
print(zahl_1 > zahl_2) # zahl_1 größer als zahl_2
print(zahl_1 < zahl_2) # zahl_1 kleiner als zahl_2
print(zahl_1 <= zahl_2) # zahl_1 kleiner gleich zahl_2
print(zahl_1 >= zahl_2) # zahl_1 größer gleich zahl_2
print(zahl_1 == zahl_2) # zahl_1 gleich zahl_2
print(zahl_1 != zahl_2) # zahl_1 ist ungleich zahl_2

# Bool'sche Operatoren
wert_a = True
print(not wert_a) # Negation von wert_a (True) also not True => False