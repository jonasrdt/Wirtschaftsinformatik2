

# Definieren der Variable 'alter' und Zuweisen des Wertes '20'
# Datentypen: Integer (int) => ganze Zahlen
alter = int(input("Bitte geben Sie Ihr Alter ein: "))
print("Ihr Alter lautet:", alter)
print("In 50 Jahren sind Sie:", alter + 50, "Jahre alt.")

# Definieren der Variable 'stundenlohn_in_euro'
# Datentyp: Float (float) => Fließkommazahlen
stundenlohn_in_euro = float(input("Bitte geben Sie Ihren Stundenlohn in Euro ein: "))
print("Ihr Stundenlohn beträgt:", stundenlohn_in_euro, "€.")
print("Wäre auch cool, wenn der Stundenlohn:", stundenlohn_in_euro + 2, "€ betrüge.")
